export function getFoodById(id) {
  return foodDatabase.find(f => f.id === id);
}

export function getFoodsByCategory(category) {
  return foodDatabase.filter(f => f.category === category);
}

export function getFoodsAvailableIn(countryCode) {
  return foodDatabase.filter(f => f.availableIn.includes(countryCode));
}

export function getFoodsCheapIn(countryCode) {
  return foodDatabase.filter(f => f.cheapIn.includes(countryCode));
}

export function getFoodsForGoal(goal) {
  return foodDatabase.filter(f => f.bestFor.includes(goal));
}

export function getFoodsSafeForUser(dietaryRestrictions) {
  return foodDatabase.filter(f =>
    !f.allergens.some(a => dietaryRestrictions.includes(a))
  );
}

export function queryFoods({ countryCode, category, dietaryRestrictions, budget, goal, travelFriendly }) {
  let results = [...foodDatabase];
  if (countryCode) results = results.filter(f => f.availableIn.includes(countryCode));
  if (category) results = results.filter(f => f.category === category);
  if (dietaryRestrictions && dietaryRestrictions.length > 0) {
    results = results.filter(f => !f.allergens.some(a => dietaryRestrictions.includes(a)));
  }
  if (budget === 'low' && countryCode) {
    results.sort((a, b) => {
      const aCheap = a.cheapIn.includes(countryCode) ? 0 : 1;
      const bCheap = b.cheapIn.includes(countryCode) ? 0 : 1;
      return aCheap - bCheap;
    });
  }
  if (goal) {
    results.sort((a, b) => {
      const aMatch = a.bestFor.includes(goal) ? 0 : 1;
      const bMatch = b.bestFor.includes(goal) ? 0 : 1;
      return aMatch - bMatch;
    });
  }
  if (travelFriendly) results = results.filter(f => f.travelFriendly);
  results.sort((a, b) => b.athleteRating - a.athleteRating);
  return results;
}

